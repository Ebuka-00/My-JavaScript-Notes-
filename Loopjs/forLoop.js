// for Loop repeat or runs the same code again  and again for a   certain number of time
//the certain number is the length of the array or list you want to loop through.
//The for loop uses that number to know how many times to repeat the code inside it.
// increment by 1 
for (let i = 0; i <= 20; i++) {
    // for loop declaration 
    console.log('Hello')
}
//increment by 2 
for (let j = 0; j <= 30; j += 2) {
    console.log(j)
}
// decrement by 2
for (let l = 6; l >= 0; l -= 2) {
    console.log(l)
}

